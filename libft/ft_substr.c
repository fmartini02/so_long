#include "libft.h"

char	*ft_substr(char *s, unsigned int start, size_t len)
{
	char	*ret;

	if (!s)
		return (0);
	if (ft_strlen(s) < start)
		len = 0;
	else if (ft_strlen(s + start) < len)
		len = ft_strlen(s + start);
	ret = malloc(sizeof(char) * (len + 1));
	if (!ret)
		return (0);
	if (ret)
	{
		ft_strlcpy(ret, s + start, len + 1);
	}
	return (ret);
}
